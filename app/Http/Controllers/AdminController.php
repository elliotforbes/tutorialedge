	{
		$input = Request::all();
        
        $article = new Article;
        
        $article->title = $input['title'];
        $article->body = $input['body'];
        $article->excerpt = $input['excerpt'];
        $article->published_at = time();
        $article->slug = "slug";
        $article->cat_id = 1;
        
        $article->save();
        
        return redirect('admin');
	}

